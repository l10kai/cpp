using namespace std;
using i64 = long long;
const char &ln = '\n';

void solve() {
    i64 a, b, n, p, q;
    cin >> n >> a >> b >> p >> q;

    i64 red = n / a;
    i64 blue = n / b;
    i64 both = n / lcm(a, b);
    red -= both;
    blue -= both;

    i64 ans = red * p + blue * q + both * max(p, q);

    cout << ans << ln;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    // cout.tie(nullptr);
    int t = 1;
    // cin >> t;

    while (t--) {
        solve();
    }
    return 0;
}
