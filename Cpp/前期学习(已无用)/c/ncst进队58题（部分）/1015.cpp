#include <iostream>

using namespace std;

int main() {
    int p;
    cin >> p;

    double b = 0.0;

    if (p <= 100000) {
        b = p * 0.1;
    } else if (p <= 200000) {
        b = 100000 * 0.1 + (p - 100000) * 0.075;
    } else if (p <= 400000) {
        b = 100000 * 0.1 + 100000 * 0.075 + (p - 200000) * 0.05;
    } else if (p <= 600000) {
        b = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (p - 400000) * 0.03;
    } else if (p <= 1000000) {
        b = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (p - 600000) * 0.015;
    } else {
        b = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (p - 1000000) * 0.01;
    }

    cout << b << endl;

    return 0;
}
